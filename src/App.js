function extractLetters(str) {
  return str.split("");
}

function findIndex(alphabet, letters, codeNum, i) {
  const indexElem = alphabet.indexOf(letters[i]);
  const code = codeNum[i];
  debugger;
  if (indexElem !== -1) {
    let chengIndex = +indexElem + +code;

    if (chengIndex === 167) {
      chengIndex = 0;
    } else if (chengIndex > 167) {
      chengIndex -= 167;
    }

    return chengIndex;
  }
}

function returnIndex(alphabet, newArray, codeNum, i) {
  const indexElem = alphabet.indexOf(newArray[i]);
  const code = codeNum[i];

  if (indexElem !== -1) {
    let chengIndex = +indexElem - +code;

    if (chengIndex === -1) {
      chengIndex = 166;
    } else if (chengIndex < -1) {
      chengIndex += 167;
    }

    return chengIndex;
  }
}

